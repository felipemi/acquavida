            <strong>Veja alguns benefícios que a musculação pode trazer:</strong>
            <p>Com a prática regular da musculação, há uma grande melhora na parte estética, pois trabalhando os músculos, você irá modelar o seu corpo. A musculação ajuda a eliminar gordura e aumenta a massa muscular, fazendo com que você fique mais saudável e esteticamente bonito.</p>
            <p>Ao treinar, você estará exercitando não só o corpo, mas também seu cérebro, que fica mais eficiente no comando da massa muscular, trazendo melhoria dos aspectos cognitivos (atenção, concentração, memória e aprendizagem).</p>
            <p>A musculação torna o coração mais saudável. Até pouco tempo, apenas as atividades aeróbias eram recomendadas para a saúde cardíaca, mas isto vem mudando. A musculação treina o coração para esforços intensos, enquanto os exercícios aeróbios preparam o coração para atividades suaves e prolongadas. Quando a pessoa fortalece os músculos, a frequência cardíaca e a pressão arterial sobem menos com o esforço. Os exercícios de musculação nos membros inferiores do nosso corpo, ajudam o sangue a retornar ao coração (músculos da perna fortes contraem durante o movimento ajudando a empurrar o fluxo sanguíneo de volta para o coração).</p>
