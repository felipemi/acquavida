            <p>Melhorias na socialização e nos relacionamentos interpessoais, pois com a sua autoestima elevada, torna-se mais confiante e de bem consigo mesmo.</p>
            <p>Melhora no bem estar, devido à liberação das endorfinas (hormônio do bem estar) e no sono, pois ganhamos um sono mais tranquilo, proporcionado pelo relaxamento dos músculos e ainda, diminui o risco de insônia.</p>
            <p>A musculação mantém você jovem. Com o avanço da idade, há uma perda de força e de massa muscular. Mas quem faz musculação, retarda esse envelhecimento. A perda da massa muscular é minimizada para quem faz exercícios com peso (musculação).</p>
            <p>A musculação ajuda a emagrecer. Todos os exercícios ajudam na perda de peso. Em todos há um gasto calórico, uns menos, outros mais. A longo prazo, os exercícios com peso apresentam um importante papel, pois aumentam a taxa metabólica basal e com o gasto calórico diário maior, queimamos mais calorias e com isso, diminuímos as reservas de gordura corporal.</p>
